# ── TTS Cache Key Consistency ─────────────────────────────


class TestTTSCacheKeyConsistency:
    """Cache key generated by preview must match render pipeline."""

    def test_same_input_same_key(self):
        """Identical inputs produce identical cache keys."""
        text = "안녕하세요, 테스트입니다."
        key1 = tts_cache_key(text, 1, "warm female voice", "korean")
        key2 = tts_cache_key(text, 1, "warm female voice", "korean")
        assert key1 == key2

    def test_different_text_different_key(self):
        """Different text produces different cache keys."""
        key1 = tts_cache_key("안녕하세요", 1, None, "korean")
        key2 = tts_cache_key("반갑습니다", 1, None, "korean")
        assert key1 != key2

    def test_different_preset_different_key(self):
        """Different voice_preset_id produces different cache keys."""
        key1 = tts_cache_key("안녕", 1, None, "korean")
        key2 = tts_cache_key("안녕", 2, None, "korean")
        assert key1 != key2

    def test_different_language_different_key(self):
        """Different language produces different cache keys."""
        key1 = tts_cache_key("hello", 1, None, "korean")
        key2 = tts_cache_key("hello", 1, None, "english")
        assert key1 != key2

    def test_different_voice_design_different_key(self):
        """Different voice_design_prompt produces different cache keys."""
        key1 = tts_cache_key("test", 1, "warm voice", "korean")
        key2 = tts_cache_key("test", 1, "cold voice", "korean")
        assert key1 != key2

    def test_none_voice_design_consistent(self):
        """None voice_design_prompt produces consistent keys."""
        key1 = tts_cache_key("test", None, None, "korean")
        key2 = tts_cache_key("test", None, None, "korean")
        assert key1 == key2

    def test_key_is_16_char_hex(self):
        """Cache key is a 16-character hex string (SHA-256 truncated)."""
        key = tts_cache_key("text", 1, "prompt", "korean")
        assert len(key) == 16
        assert all(c in "0123456789abcdef" for c in key)

    def test_key_matches_sha256_truncation(self):
        """Cache key matches manual SHA-256 calculation (includes naturalness suffix)."""
        from config import TTS_NATURALNESS_SUFFIX

        text, pid, design, lang, emotion = "hello", 1, "warm", "korean", ""
        # speaker=None → 빈 문자열
        parts = f"{text}|{pid}|{design}|{lang}|{emotion}||{TTS_NATURALNESS_SUFFIX}"
        expected = hashlib.sha256(parts.encode()).hexdigest()[:16]
        actual = tts_cache_key(text, pid, design, lang)
        assert actual == expected

    def test_cleaned_text_matches_render(self):
        """Preview cleans text same way as render, producing same cache key."""
        raw = "(한숨) 오늘도 힘든 하루였어..."
        cleaned = clean_script_for_tts(raw)
        # Preview and render both call clean_script_for_tts before tts_cache_key
        key = tts_cache_key(cleaned, None, None, "korean")
        assert len(key) == 16

    def test_none_preset_id_uses_none_string(self):
        """None preset_id is included as 'None' in the hash input."""
        from config import TTS_NATURALNESS_SUFFIX

        # speaker=None → 빈 문자열 (2번째 | 뒤)
        parts_none = f"test|{None}|{''}|korean|||{TTS_NATURALNESS_SUFFIX}"
        expected = hashlib.sha256(parts_none.encode()).hexdigest()[:16]
        actual = tts_cache_key("test", None, None, "korean")
        assert actual == expected


