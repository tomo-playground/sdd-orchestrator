# ── WAV Duration Helper ──────────────────────────────────


class TestWavDuration:
    """Test _wav_duration helper."""

    def test_calculates_correct_duration(self):
        """WAV duration is correctly calculated from bytes."""
        from services.video.tts_helpers import _wav_duration

        buf = io.BytesIO()
        with wave.open(buf, "w") as wf:
            wf.setnchannels(1)
            wf.setsampwidth(2)
            wf.setframerate(44100)
            wf.writeframes(b"\x00\x00" * 44100 * 2)  # 2 seconds

        duration = _wav_duration(buf.getvalue())
        assert abs(duration - 2.0) < 0.01

    def test_stereo_wav(self):
        """Stereo WAV duration is correctly calculated."""
        from services.video.tts_helpers import _wav_duration

        buf = io.BytesIO()
        with wave.open(buf, "w") as wf:
            wf.setnchannels(2)
            wf.setsampwidth(2)
            wf.setframerate(22050)
            # 2 channels * 2 bytes/sample * 22050 samples = 1 second
            wf.writeframes(b"\x00\x00" * 22050 * 2)

        duration = _wav_duration(buf.getvalue())
        assert abs(duration - 1.0) < 0.01


