class TestPreviewTimeline:
    """Test timeline calculation."""

    def test_basic_timeline(self):
        """Basic timeline with TTS and non-TTS scenes."""
        from services.preview import preview_timeline

        req = TimelineRequest(
            scenes=[
                TimelineSceneInput(script="안녕하세요", duration=3.0, tts_duration=2.0),
                TimelineSceneInput(script="반갑습니다", duration=3.0, tts_duration=1.5),
                TimelineSceneInput(script="", duration=3.0, tts_duration=None),
            ],
            speed_multiplier=1.0,
        )

        result = preview_timeline(req)
        assert len(result.scenes) == 3
        assert result.total_duration > 0

        # Scenes should be sequential
        for i in range(1, len(result.scenes)):
            assert result.scenes[i].start_time == result.scenes[i - 1].end_time

    def test_empty_scenes(self):
        """Empty scene list produces zero duration."""
        from services.preview import preview_timeline

        req = TimelineRequest(scenes=[], speed_multiplier=1.0)
        result = preview_timeline(req)
        assert result.total_duration == 0
        assert len(result.scenes) == 0

    def test_speed_multiplier_affects_duration(self):
        """Higher speed multiplier reduces total duration."""
        from services.preview import preview_timeline

        scenes = [TimelineSceneInput(script="", duration=4.0, tts_duration=None)]

        slow = preview_timeline(TimelineRequest(scenes=scenes, speed_multiplier=0.5))
        normal = preview_timeline(TimelineRequest(scenes=scenes, speed_multiplier=1.0))
        fast = preview_timeline(TimelineRequest(scenes=scenes, speed_multiplier=2.0))

        assert slow.total_duration > normal.total_duration
        assert normal.total_duration > fast.total_duration

    def test_tts_duration_extends_scene(self):
        """TTS duration extends scene when it exceeds base duration."""
        from services.preview import preview_timeline

        # Short base duration + long TTS should extend
        req = TimelineRequest(
            scenes=[
                TimelineSceneInput(script="긴 대사입니다", duration=2.0, tts_duration=5.0),
            ],
            speed_multiplier=1.0,
        )

        result = preview_timeline(req)
        assert result.scenes[0].has_tts is True
        # Effective duration should be at least the TTS duration
        assert result.scenes[0].effective_duration >= 5.0

    def test_no_tts_uses_base_duration(self):
        """Scene without TTS uses base duration / speed."""
        from services.preview import preview_timeline

        req = TimelineRequest(
            scenes=[
                TimelineSceneInput(script="", duration=4.0, tts_duration=None),
            ],
            speed_multiplier=1.0,
        )

        result = preview_timeline(req)
        assert result.scenes[0].has_tts is False
        assert result.scenes[0].effective_duration == 4.0

    def test_total_duration_equals_sum(self):
        """Total duration equals sum of effective durations."""
        from services.preview import preview_timeline

        req = TimelineRequest(
            scenes=[
                TimelineSceneInput(script="", duration=3.0, tts_duration=None),
                TimelineSceneInput(script="", duration=5.0, tts_duration=None),
            ],
            speed_multiplier=1.0,
        )

        result = preview_timeline(req)
        expected = sum(s.effective_duration for s in result.scenes)
        assert abs(result.total_duration - expected) < 0.01

    def test_start_end_times_consistent(self):
        """Each scene start_time == previous end_time."""
        from services.preview import preview_timeline

        req = TimelineRequest(
            scenes=[
                TimelineSceneInput(script="대사", duration=3.0, tts_duration=2.0),
                TimelineSceneInput(script="", duration=4.0, tts_duration=None),
                TimelineSceneInput(script="대사", duration=2.0, tts_duration=3.0),
            ],
            speed_multiplier=1.0,
        )

        result = preview_timeline(req)

        # First scene starts at 0
        assert result.scenes[0].start_time == 0.0

        for i in range(1, len(result.scenes)):
            assert result.scenes[i].start_time == result.scenes[i - 1].end_time

        # Last scene end_time equals total_duration
        assert result.scenes[-1].end_time == result.total_duration

    def test_speed_multiplier_clamped(self):
        """Speed multiplier is clamped between 0.25 and 2.0."""
        from services.preview import preview_timeline

        scenes = [TimelineSceneInput(script="", duration=4.0, tts_duration=None)]

        # Very low speed is clamped to 0.25
        very_slow = preview_timeline(TimelineRequest(scenes=scenes, speed_multiplier=0.01))
        clamped_slow = preview_timeline(TimelineRequest(scenes=scenes, speed_multiplier=0.25))

        assert very_slow.total_duration == clamped_slow.total_duration

        # Very high speed is clamped to 2.0
        very_fast = preview_timeline(TimelineRequest(scenes=scenes, speed_multiplier=10.0))
        clamped_fast = preview_timeline(TimelineRequest(scenes=scenes, speed_multiplier=2.0))

        assert very_fast.total_duration == clamped_fast.total_duration


